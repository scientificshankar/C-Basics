#include<stdio.h>
void main ()
 {
 	int n,k,l,j;
 	n=100;
 	k= n*(n+1)/2;
 	l=n*(n+1);
 	j=n*n;
 
 	
 	printf("sum of first %d natural numbers is %d\n" ,n,k);
 	
 	printf("sum of first %d even natural numbers is %d\n", n, l);
 	
 	printf("sum of first %d odd numbers is %d", n, j);
 	
 	
 }
 
  
 		
 	 
 	 
